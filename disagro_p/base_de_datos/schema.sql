CREATE TABLE "TOMA_FISICA".USUARIO(
    ID_USUARIO SERIAL PRIMARY KEY,
    USUARIO VARCHAR(100) NOT NULL UNIQUE,
    CONTRASENA VARCHAR(255) NOT NULL,
    NOMBRE VARCHAR(255) NOT NULL,
    SUPER_USUARIO VARCHAR(255) NOT NULL,
    NIVEL_1 VARCHAR(2) NOT NULL, -- GERENTE
    NIVEL_2 VARCHAR(2) NOT NULL, -- CONTABLE
    NIVEL_3 VARCHAR(2) NOT NULL, -- JEFE DE ALMACEN
    NIVEL_4 VARCHAR(2) NOT NULL, -- SUPERVISOR
    NIVEL_5 VARCHAR(2) NOT NULL, -- CAPTACION FISICA
    PAIS VARCHAR(50) NOT NULL
); 

CREATE TABLE "TOMA_FISICA"."BITACORA" (
    "ID_BITACORA" SERIAL PRIMARY KEY,
    "ID_USUARIO" VARCHAR(255) NOT NULL REFERENCES "TOMA_FISICA".usuario(usuario),
    "ACCION" VARCHAR(255) NOT NULL,
    "TABLA" VARCHAR(50) NOT NULL,
    "FECHA_REGISTRO" TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    "DETALLES" TEXT
);

