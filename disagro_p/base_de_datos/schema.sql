CREATE TABLE "TOMA_FISICA"."CAPTACION_FISICA" (
    "ID" SERIAL PRIMARY KEY,
    "ID_PLANIFICACION" INTEGER NOT NULL,
    "ARTICULO" VARCHAR(20),
    "DESCRIPCION" VARCHAR(254),
    "UBICACION" VARCHAR(50),
    "ALMACEN" VARCHAR(50),
    "LOTE" VARCHAR(50),
    "FECHA_EXPIRACION" TIMESTAMP NULL,
    "ETIQUETA" VARCHAR(50), -- "NUEVO" VARCHAR(50)
    "FECHA" TIMESTAMP NULL,
    "CANTIDAD" DECIMAL,
    "USUARIO" VARCHAR(50),
    "IMAGEN" BYTEA,
    "ESTADO" VARCHAR(50), 
    "SERIE" VARCHAR(100),
    "MODELO" VARCHAR(100),
    "OBSERVACION" VARCHAR(50),
    FOREIGN KEY ("ARTICULO") REFERENCES "TOMA_FISICA"."ARTICULO" ("ARTICULO"),
    FOREIGN KEY ("UBICACION") REFERENCES "TOMA_FISICA"."UBICACION" ("UBICACION"),
    FOREIGN KEY ("ALMACEN") REFERENCES "TOMA_FISICA"."ALMACEN" ("ALMACEN"),
    FOREIGN KEY ("ID_PLANIFICACION") REFERENCES "TOMA_FISICA"."PLANIFICACION" ("ID")
);

CREATE TABLE "TOMA_FISICA"."EXISTENCIA_UBICACION" (
    "ID" SERIAL PRIMARY KEY, 
    "ARTICULO" VARCHAR(20),
    "ID_PLANIFICACION" INTEGER NOT NULL,
    "UBICACION" VARCHAR(50),
    "ALMACEN" VARCHAR(50), 
    "LOTE" VARCHAR(50),
    "FECHA_EXPIRACION" TIMESTAMP NULL,
    "CANTIDAD" DECIMAL,
    "COSTO" DECIMAL,
    FOREIGN KEY ("ARTICULO") REFERENCES "TOMA_FISICA"."ARTICULO" ("ARTICULO"),
    FOREIGN KEY ("UBICACION") REFERENCES "TOMA_FISICA"."UBICACION" ("UBICACION"),
    FOREIGN KEY ("ALMACEN") REFERENCES "TOMA_FISICA"."ALMACEN" ("ALMACEN"),
    FOREIGN KEY ("ID_PLANIFICACION") REFERENCES "TOMA_FISICA"."PLANIFICACION" ("ID")
);

